        guard let cell = tableView.dequeueReusableCell(withIdentifier: "EntryCell", for: indexPath) as? EntryTableViewCell else { return UITableViewCell() }

        cell.entry = fetchedResultsController.object(at: indexPath)

        return cell
    }

